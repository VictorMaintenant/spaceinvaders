package fr.unilim.iut.spaceinvaders;

public class Dimensions {
    int longueur;
	int hauteur;

	public Dimensions(int longueur, int hauteur) {
		this.longueur = longueur;
		this.hauteur = hauteur;
	}

	public int longueur() {
		return this.longueur;
	}

	public int hauteur() {
		return this.hauteur;
	}

 }   
